public class PerKnowledge {
    private String name;
    private String kname;
    private String rtype;
    private String rgrade;
    private String rp_desc;
    private String puuid;
    private String kuuid;
    private String source;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKname() {
        return kname;
    }

    public void setKname(String kname) {
        this.kname = kname;
    }

    public String getRtype() {
        return rtype;
    }

    public void setRtype(String rtype) {
        this.rtype = rtype;
    }

    public String getRgrade() {
        return rgrade;
    }

    public void setRgrade(String rgrade) {
        this.rgrade = rgrade;
    }

    public String getRp_desc() {
        return rp_desc;
    }

    public void setRp_desc(String rp_desc) {
        this.rp_desc = rp_desc;
    }

    public String getPuuid() {
        return puuid;
    }

    public void setPuuid(String puuid) {
        this.puuid = puuid;
    }

    public String getKuuid() {
        return kuuid;
    }

    public void setKuuid(String kuuid) {
        this.kuuid = kuuid;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }
}
